package model;

public class Bloco {

    private String texto;

    public Bloco() {
        this.texto = "";
    }

    public Bloco(String texto) {
        this.texto = texto;
    }

    public String getTexto() {
        return texto;
    }

    public void setTexto(String texto) {
        this.texto = texto;
    }

}
